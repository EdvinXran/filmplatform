package com.stylefeng.guns.rest.persistence.model.bo.cinemabo;

import java.io.Serializable;
import java.util.List;

public class CinemaFilmBO implements Serializable {

    private static final long serialVersionUID = 7693823077853085420L;
    private int filmId;
    private String filmName;
    private String filmLength;
    private String filmType;
    private String filmCats;
    private String actors;
    private String imgAddress;
    private List<CinemaFieldBO> filmFields;

    public CinemaFilmBO() {
    }

    public int getFilmId() {
        return filmId;
    }

    public void setFilmId(int filmId) {
        this.filmId = filmId;
    }

    public String getFilmName() {
        return filmName;
    }

    public void setFilmName(String filmName) {
        this.filmName = filmName;
    }

    public String getFilmLength() {
        return filmLength;
    }

    public void setFilmLength(String filmLength) {
        this.filmLength = filmLength;
    }

    public String getFilmType() {
        return filmType;
    }

    public void setFilmType(String filmType) {
        this.filmType = filmType;
    }

    public String getFilmCats() {
        return filmCats;
    }

    public void setFilmCats(String filmCats) {
        this.filmCats = filmCats;
    }

    public String getActors() {
        return actors;
    }

    public void setActors(String actors) {
        this.actors = actors;
    }

    public String getImgAddress() {
        return imgAddress;
    }

    public void setImgAddress(String imgAddress) {
        this.imgAddress = imgAddress;
    }

    public List<CinemaFieldBO> getFilmFields() {
        return filmFields;
    }

    public void setFilmFields(List<CinemaFieldBO> filmFields) {
        this.filmFields = filmFields;
    }


}